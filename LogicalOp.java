package Operators;

public class LogicalOp {

	public static void main(String[] args) {
		int a,b;
		a=10;
		b=15;
		System.out.println(a>b||b>a);//logical or operator
		System.out.println(a<b&&b>a);//logical and operator
		System.out.println(a>b&&b>a);//logical and operator
		
	}

}
