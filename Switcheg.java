package Activity;

public class Switcheg {

	public static void main(String[] args) {
		int a=8;
		switch(a) {
		case 1:System.out.println("11");
		break;
		case 2:System.out.println("60");
		break;
		case 8:System.out.println("10");
		break;
		case 6:System.out.println("19");
		break;
		case 9:System.out.println("456");
		break;
		default:System.out.println("invalid number");
		}

	}

}
